package org.limmen.calgen.domain;

import java.awt.Color;

public class ColorSet {

  private Color color;
  private int set;

  public Color getColor() {
    return color;
  }

  public int getSet() {
    return set;
  }

  public void setColor(Color color) {
    this.color = color;
  }

  public void setSet(int set) {
    this.set = set;
  }  
}
